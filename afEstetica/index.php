    function menuShow(x) {
      x.classList.toggle("change");
      let menu = document.getElementsByClassName("menu-alt")[0];
      let pos = document.documentElement.scrollTop;
      let hg = document.getElementsByClassName("carousel")[0].offsetHeight;
      if(window.getComputedStyle(menu).display === "none"){
        menu.style.display = "inline";   
        document.getElementsByClassName("nav-fix")[0].style.backgroundColor = "transparent";
        document.getElementsByClassName("nav-fix")[0].style.color = "white";
        document.getElementsByClassName("bar1")[0].style.backgroundColor = "white";
        document.getElementsByClassName("bar2")[0].style.backgroundColor = "white";
        document.getElementsByClassName("bar3")[0].style.backgroundColor = "white";
      } else {
        menu.style.display = "none";
        console.log(hg);
        if(pos >= hg-200){
            document.getElementsByClassName("nav-fix")[0].style.backgroundColor = "white";
            document.getElementsByClassName("nav-fix")[0].style.color = "rgb(100,100,100)";
            document.getElementsByClassName("bar1")[0].style.backgroundColor = "rgb(100,100,100)";
            document.getElementsByClassName("bar2")[0].style.backgroundColor = "rgb(100,100,100)";
            document.getElementsByClassName("bar3")[0].style.backgroundColor = "rgb(100,100,100)";
        }
      }
    }
      
    function sectionShow(){
        let x = document.getElementsByClassName("menu-btn-wrapper");
        x.item(0).classList.toggle("change");
        document.getElementsByClassName("menu-alt")[0].style.display = "none";
        if(window.scrollTop >= document.getElementsByClassName("carousel")[0].height - 200){
            document.getElementsByClassName("nav-fix")[0].style.backgroundColor = "white";
        } else {
            document.getElementsByClassName("nav-fix")[0].style.backgroundColor = "transparent";
        }
    }
	
